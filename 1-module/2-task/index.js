/**
 * Эту функцию нужно поменять так,
 * чтобы функция sayHello работала корректно
 */
function isValid(name) {
  let space = name.includes(' ');
  let nameLehgth = name.length;
  if (!!name && nameLehgth >= 4 && !space) {
    return true;
  }
  return false;
}

